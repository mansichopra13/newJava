public class ternary 
{           //short if else
    //variable = (condition) ? expressionTrue:expressionFalse;
    public static void main(String [] args)
    {
        int x=5;
        int result=(x<=5) ? 1:2;
        System.out.println(result);

        int y=7;
        String ew = (y<10)? "yes":"no";

        System.out.println(ew);
    }
    
}
