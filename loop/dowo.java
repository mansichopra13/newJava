package loop;
//loops can save time , reduce error and make code more readable
public class dowo 
{
    public static void main (String [] args)
    {
        int i=0;
        // do loop will be executed once even if the condition is false
        do
        {
            System.out.println(i);
            i++;
        
        }
        while(i<5);
    }
}
