package loop;
public class fr 
{
    public static void main(String [] args)
    {
        //when we know how many times we want to run the loop we use for loop
        
        for(int i=0;i<5;i++)
        {
            System.out.println(i);
        }
        
        for(int i=1;i<10;i+=3)
        {
            System.out.println(i);
        }
    }
    
}
